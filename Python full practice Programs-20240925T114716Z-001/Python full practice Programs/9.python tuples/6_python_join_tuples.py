# Python Join Tuples

# to join two or more tuples " + " you can use the addition operator
tuple1 = ('shayan','haider','faisal','komal','aiyza','bushra')
tuple2 = ('red' , 'greed','blue', 56,45,34,23)
print(tuple1 + tuple2) # output ('shayan', 'haider', 'faisal', 'komal', 'aiyza', 'bushra', 'red', 'greed', 'blue', 56, 45, 34, 23)
print()

# Multiply Tuples
# If you want to multiply the content of a tuple a given number of times, you can use the * operator:

index = ('dow jones','nasdaq','s&p500')
doublingtuple = index *2
print(doublingtuple) # output ('dow jones', 'nasdaq', 's&p500', 'dow jones', 'nasdaq', 's&p500')
print()
